package com.example.spit_hackathon_ecoquest.Models;

public class Waste {
    String dryWaste, wetWaste, others;

    public String getDryWaste() {
        return dryWaste;
    }

    public void setDryWaste(String dryWaste) {
        this.dryWaste = dryWaste;
    }

    public String getWetWaste() {
        return wetWaste;
    }

    public void setWetWaste(String wetWaste) {
        this.wetWaste = wetWaste;
    }

    public String getOthers() {
        return others;
    }

    public void setOthers(String others) {
        this.others = others;
    }
}
